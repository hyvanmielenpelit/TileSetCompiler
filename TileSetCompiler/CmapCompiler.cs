using System;
using System.Collections.Generic;
using System.Drawing;
using System.IO;
using System.Text;
using TileSetCompiler.Creators;
using TileSetCompiler.Creators.Data;

namespace TileSetCompiler
{
    class CmapCompiler : BitmapCompiler
    {
        const string _subDirName = "Cmap";
        const string _unknownFileName = "UnknownCmap.png";
        const int _lineLength = 3;
        const float _darknessOpacity = 0.75f;

        private Dictionary<string, CmapDarknessCreatorData> _autogeneratedDarkTiles = new Dictionary<string, CmapDarknessCreatorData>()
        {
            { "darkroom", new CmapDarknessCreatorData("room", _darknessOpacity) }
        };

        public DarknessCreator DarknessCreator { get; private set; }

        public CmapCompiler(StreamWriter tileNameWriter) : base(_subDirName, _unknownFileName, tileNameWriter)
        {
            DarknessCreator = new DarknessCreator(_subDirName, _unknownFileName);
        }

        public override void CompileOne(string[] splitLine)
        {
            if (splitLine.Length < _lineLength)
            {
                throw new Exception(string.Format("Cmap line '{0}' has too few elements.", string.Join(',', splitLine)));
            }

            var map = splitLine[1];
            var name = splitLine[2];
            var subDir2 = map.ToLower().Replace(" ", "_");

            var dirPath = Path.Combine(BaseDirectory.FullName, subDir2);
            FileInfo usedFile = null;
            var fileName = map.ToLower().Replace(" ", "_") + "_" + name.Substring(2).ToLower().Replace(" ", "_") + Program.ImageFileExtension;
            var relativePath = Path.Combine(_subDirName, subDir2, fileName);
            var cmapName = name.Substring(2).ToLower().Replace(" ", "_");

            if (!Directory.Exists(dirPath))
            {
                Console.WriteLine("Cmap directory '{0}' not found. Using Unknown Cmap icon.", dirPath);
                usedFile = UnknownFile;
                WriteTileNameErrorDirectoryNotFound(relativePath, "Using Unknown Cmap icon.");

                using (var image = new Bitmap(Image.FromFile(usedFile.FullName)))
                {
                    DrawImageToTileSet(image);
                    IncreaseCurXY();
                }
            }
            else
            {
                var filePath = Path.Combine(dirPath, fileName);
                FileInfo file = new FileInfo(filePath);

                if (_autogeneratedDarkTiles.ContainsKey(cmapName))
                {
                    var info = _autogeneratedDarkTiles[cmapName];
                    var sourceCmapName = info.OriginalCmapName.ToLower().Replace(" ", "_");
                    var sourceFileName = map.ToLower().Replace(" ", "_") + "_" + sourceCmapName + Program.ImageFileExtension;
                    var sourceFilePath = Path.Combine(dirPath, sourceFileName);
                    var sourceFile = new FileInfo(sourceFilePath);

                    bool isUnknown;
                    using (var image = DarknessCreator.CreateDarkBitmapFromFile(sourceFile, info.Opacity, out isUnknown))
                    {
                        if (!isUnknown)
                        {
                            WriteTileNameAutogenerationSuccess(sourceFilePath, relativePath, "cmap");
                        }
                        else
                        {
                            Console.WriteLine("Cmap file '{0}' not found for darkness creation. Using Unknown Cmap icon.", sourceFile.FullName);
                            WriteTileNameAutogenerationError(sourceFilePath, relativePath, "cmap");
                        }

                        DrawImageToTileSet(image);
                        IncreaseCurXY();
                    }
                }
                else
                {
                    if (file.Exists)
                    {
                        usedFile = file;
                        WriteTileNameSuccess(relativePath);
                    }
                    else
                    {
                        Console.WriteLine("File '{0}' not found. Using Unknown Cmap icon.", file.FullName);
                        usedFile = UnknownFile;
                        WriteTileNameErrorFileNotFound(relativePath, "Using Unknown Cmap icon.");
                    }                    

                    using (var image = new Bitmap(Image.FromFile(usedFile.FullName)))
                    {
                        DrawImageToTileSet(image);
                        IncreaseCurXY();
                    }
                }
            }
            
        }
    }
}
